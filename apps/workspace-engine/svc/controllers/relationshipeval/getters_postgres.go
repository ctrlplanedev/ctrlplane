type PostgresGetter struct{}

func (g *PostgresGetter) GetEntityInfo(ctx context.Context, entityType string, entityID uuid.UUID) (*EntityInfo, error) {
	pool := db.GetPool(ctx)

	switch entityType {
	case "resource":
		var info EntityInfo
		err := pool.QueryRow(ctx, getResourceSQL, entityID).Scan(
			&info.ID, &info.WorkspaceID,
			new(string), new(string), new(string), new(string),
			new(*string), new(map[string]any), new(map[string]string),
			new(pgtype.Timestamptz), new(pgtype.Timestamptz),
		)
		if err != nil {
			return nil, fmt.Errorf("get resource %s: %w", entityID, err)
		}
		info.EntityType = "resource"
		info.Raw = buildResourceMap(ctx, entityID, pool)
		return &info, nil

	case "deployment":
		var info EntityInfo
		err := pool.QueryRow(ctx, getDeploymentSQL, entityID).Scan(
			&info.ID, &info.WorkspaceID,
			new(string), new(string), new(*string),
			new(*string), new(map[string]any),
		)
		if err != nil {
			return nil, fmt.Errorf("get deployment %s: %w", entityID, err)
		}
		info.EntityType = "deployment"
		info.Raw = buildDeploymentMap(ctx, entityID, pool)
		return &info, nil

	case "environment":
		var info EntityInfo
		err := pool.QueryRow(ctx, getEnvironmentSQL, entityID).Scan(
			&info.ID, &info.WorkspaceID,
			new(string), new(*string),
			new(pgtype.Timestamptz),
		)
		if err != nil {
			return nil, fmt.Errorf("get environment %s: %w", entityID, err)
		}
		info.EntityType = "environment"
		info.Raw = buildEnvironmentMap(ctx, entityID, pool)
		return &info, nil

	default:
		return nil, fmt.Errorf("unknown entity type %q for id %s", entityType, entityID)
	}
}

func (g *PostgresGetter) GetRulesForWorkspace(ctx context.Context, workspaceID uuid.UUID) ([]RuleInfo, error) {
	pool := db.GetPool(ctx)
	q := db.New(pool)

	rows, err := q.GetRelationshipRulesForWorkspace(ctx, workspaceID)
	if err != nil {
		return nil, fmt.Errorf("get rules for workspace %s: %w", workspaceID, err)
	}

	rules := make([]RuleInfo, 0, len(rows))
	for _, row := range rows {
		fromType, toType, err := parseTypesFromCel(row.Cel)
		if err != nil {
			return nil, fmt.Errorf("parse types from CEL for rule %s: %w", row.ID, err)
		}
		rules = append(rules, RuleInfo{
			ID:        row.ID,
			Reference: row.Reference,
			Cel:       row.Cel,
			FromType:  fromType,
			ToType:    toType,
		})
	}
	return rules, nil
}

var celTypePattern = regexp.MustCompile(`^from\.type\s*==\s*"([^"]+)"\s*&&\s*to\.type\s*==\s*"([^"]+)"`)

// parseTypesFromCel extracts the from/to entity types from a compiled CEL
// expression. The expression is expected to start with the type guards
// generated by ruleToCelExpression: from.type == "X" && to.type == "Y".
func parseTypesFromCel(cel string) (fromType, toType string, err error) {
	m := celTypePattern.FindStringSubmatch(cel)
	if m == nil {
		return "", "", fmt.Errorf("CEL expression does not start with expected type guards: %q", cel)
	}
	return m[1], m[2], nil
}

func (g *PostgresGetter) StreamCandidateEntities(ctx context.Context, workspaceID uuid.UUID, entityType string, batchSize int, batches chan<- []EntityInfo) error {
	defer close(batches)

	var query string
	var buildMap func(row []any) map[string]any
	switch entityType {
	case "resource":
		query = listResourcesSQL
		buildMap = rowToResourceMap
	case "deployment":
		query = listDeploymentsSQL
		buildMap = rowToDeploymentMap
	case "environment":
		query = listEnvironmentsSQL
		buildMap = rowToEnvironmentMap
	default:
		return fmt.Errorf("unknown entity type: %s", entityType)
	}

	rows, err := db.GetPool(ctx).Query(ctx, query, workspaceID)
	if err != nil {
		return fmt.Errorf("query %s entities for workspace %s: %w", entityType, workspaceID, err)
	}
	defer rows.Close()

	batch := make([]EntityInfo, 0, batchSize)
	for rows.Next() {
		values, err := rows.Values()
		if err != nil {
			return fmt.Errorf("scan %s row: %w", entityType, err)
		}

		id, ok := values[0].([16]byte)
		if !ok {
			continue
		}
		entityID := uuid.UUID(id)

		wsID, ok := values[1].([16]byte)
		if !ok {
			continue
		}

		batch = append(batch, EntityInfo{
			ID:          entityID,
			WorkspaceID: uuid.UUID(wsID),
			EntityType:  entityType,
			Raw:         buildMap(values),
		})

		if len(batch) >= batchSize {
			select {
			case batches <- batch:
			case <-ctx.Done():
				return ctx.Err()
			}
			batch = make([]EntityInfo, 0, batchSize)
		}
	}

	if err := rows.Err(); err != nil {
		return fmt.Errorf("iterate %s entities: %w", entityType, err)
	}

	if len(batch) > 0 {
		select {
		case batches <- batch:
		case <-ctx.Done():
			return ctx.Err()
		}
	}

	return nil
}


// --- SQL ---

const getResourceSQL = `
SELECT id, workspace_id, name, kind, version, identifier,
       provider_id, config, metadata, created_at, updated_at
FROM resource
WHERE id = $1 AND deleted_at IS NULL
`

const getDeploymentSQL = `
SELECT id, workspace_id, name, slug, description, job_agent_id, job_agent_config
FROM deployment
WHERE id = $1
`

const getEnvironmentSQL = `
SELECT id, workspace_id, name, description, created_at
FROM environment
WHERE id = $1
`

const listResourcesSQL = `
SELECT id, workspace_id, name, kind, version, identifier,
       provider_id, config, metadata, created_at, updated_at
FROM resource
WHERE workspace_id = $1 AND deleted_at IS NULL
`

const listDeploymentsSQL = `
SELECT id, workspace_id, name, slug, description, job_agent_id, job_agent_config, metadata
FROM deployment
WHERE workspace_id = $1
`

const listEnvironmentsSQL = `
SELECT id, workspace_id, name, description, metadata, created_at
FROM environment
WHERE workspace_id = $1
`

// --- Map builders (single-row lookups via pool.QueryRow) ---

func buildResourceMap(ctx context.Context, id uuid.UUID, pool *pgxpool.Pool) map[string]any {
	var (
		name, kind, version, identifier string
		providerID                      *string
		config                          map[string]any
		metadata                        map[string]string
		createdAt, updatedAt            pgtype.Timestamptz
	)
	_ = pool.QueryRow(ctx, getResourceSQL, id).Scan(
		new(uuid.UUID), new(uuid.UUID),
		&name, &kind, &version, &identifier,
		&providerID, &config, &metadata, &createdAt, &updatedAt,
	)
	m := map[string]any{
		"type":       "resource",
		"id":         id.String(),
		"name":       name,
		"kind":       kind,
		"version":    version,
		"identifier": identifier,
		"config":     config,
		"metadata":   stringMapToAnyMap(metadata),
	}
	if providerID != nil {
		m["providerId"] = *providerID
	}
	return m
}

func buildDeploymentMap(ctx context.Context, id uuid.UUID, pool *pgxpool.Pool) map[string]any {
	var (
		name, slug   string
		description  *string
		jobAgentID   *string
		jobAgentConf map[string]any
	)
	_ = pool.QueryRow(ctx, getDeploymentSQL, id).Scan(
		new(uuid.UUID), new(uuid.UUID),
		&name, &slug, &description, &jobAgentID, &jobAgentConf,
	)
	m := map[string]any{
		"type":           "deployment",
		"id":             id.String(),
		"name":           name,
		"slug":           slug,
		"jobAgentConfig": jobAgentConf,
	}
	if description != nil {
		m["description"] = *description
	}
	if jobAgentID != nil {
		m["jobAgentId"] = *jobAgentID
	}
	return m
}

func buildEnvironmentMap(ctx context.Context, id uuid.UUID, pool *pgxpool.Pool) map[string]any {
	var (
		name        string
		description *string
		createdAt   pgtype.Timestamptz
	)
	_ = pool.QueryRow(ctx, getEnvironmentSQL, id).Scan(
		new(uuid.UUID), new(uuid.UUID),
		&name, &description, &createdAt,
	)
	m := map[string]any{
		"type": "environment",
		"id":   id.String(),
		"name": name,
	}
	if description != nil {
		m["description"] = *description
	}
	return m
}

// --- Map builders (from row values in streaming) ---

func rowToResourceMap(values []any) map[string]any {
	m := map[string]any{
		"type": "resource",
	}
	if len(values) > 0 {
		if id, ok := values[0].([16]byte); ok {
			m["id"] = uuid.UUID(id).String()
		}
	}
	if len(values) > 2 {
		m["name"] = values[2]
	}
	if len(values) > 3 {
		m["kind"] = values[3]
	}
	if len(values) > 4 {
		m["version"] = values[4]
	}
	if len(values) > 5 {
		m["identifier"] = values[5]
	}
	if len(values) > 7 {
		m["config"] = values[7]
	}
	if len(values) > 8 {
		if md, ok := values[8].(map[string]string); ok {
			m["metadata"] = stringMapToAnyMap(md)
		}
	}
	return m
}

